<h2>Spaces</h2>

<ul class="api">
	<li>
		<code>Space->isEmpty</code>
		<span>If true, this means that nothing (except maybe stairs) is at this location and you can walk here.</span>
	</li>
	<li>
		<code>Space->isWall</code>
		<span>Checks if the space is a wall.</span>
	</li>
	<li>
		<code>Space->isEnnemy</code>
		<span>Determine if an ennemy unit is at this location.</span>
	</li>
</ul>